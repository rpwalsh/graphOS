// ── Render API ────────────────────────────────────────────────────────────────

/// Blit a single ASCII glyph into a BGRA32 pixel buffer.
///
/// - `buf`: BGRA32 pixel slice (row-major).
/// - `stride`: pixels per row in `buf`.
/// - `x`, `y`: top-left pixel coordinate.
/// - `fg`: foreground colour (BGRA32, alpha ignored).
/// - `bg`: background colour; pass `0` to skip background fill (transparent).
pub fn draw_glyph(buf: &mut [u32], stride: usize, x: usize, y: usize, ch: u8, fg: u32, bg: u32) {
    let idx = if (FIRST_CHAR..=LAST_CHAR).contains(&ch) {
        (ch - FIRST_CHAR) as usize
    } else {
        // Default to a small square for unmapped characters.
        GLYPH_COUNT - 1
    };
    let rows = &FONT_ATLAS[idx];
    for (row, &bits) in rows.iter().enumerate().take(GLYPH_H) {
        let py = y + row;
        for col in 0..GLYPH_W {
            let px = x + col;
            let pixel_on = (bits >> (7 - col)) & 1 != 0;
            let pixel = if pixel_on { fg } else { bg };
            if pixel == 0 {
                continue;
            } // transparent background
            let off = py * stride + px;
            if off < buf.len() {
                buf[off] = pixel;
            }
        }
    }
}

/// Render a null-terminated or length-bounded ASCII string.
///
/// Returns the x coordinate after the last glyph (useful for cursor tracking).
pub fn draw_str(
    buf: &mut [u32],
    stride: usize,
    x: usize,
    y: usize,
    text: &[u8],
    fg: u32,
    bg: u32,
) -> usize {
    let mut cx = x;
    for &ch in text {
        if ch == 0 {
            break;
        }
        draw_glyph(buf, stride, cx, y, ch, fg, bg);
        cx += GLYPH_W;
    }
    cx
}

/// Measure the pixel width of a string (does not render).
pub fn measure(text: &[u8]) -> usize {
    text.iter().take_while(|&&c| c != 0).count() * GLYPH_W
}
