//! Phase J Session 30 — In-kernel bitmap font renderer.
//!
//! Provides a minimal, zero-allocation bitmap font system suitable for
//! kernel-side text rendering (early boot console, crash screen, debug HUD).
//! Ring-3 applications use the heavier `sdk/ui-sdk/` font stack instead.
//!
//! ## Design
//! - Built-in 8×16 monospace glyph atlas covering printable ASCII (0x20–0x7E).
//! - BGRA32 pixel output: each glyph is blitted into a caller-supplied buffer.
//! - No heap allocation; no external dependencies.
//!
//! ## Phase J Session 30 status
//! - Built-in 8×16 monospace atlas: ✅ IMPLEMENTED (reduced ASCII set; full
//!   TrueType/subpixel antialiasing deferred to ring-3 `ui-sdk` in v1.1).
//! - Glyph atlas GPU texture upload: ⬜ deferred (virtio-gpu compute path).
//! - Variable-width, TrueType outline parsing: ⬜ deferred (Session 30 full impl).
//!
//! ## Limitations
//! This is the kernel-resident *stub* font renderer.  It is sufficient for:
//! - Crash / panic screens
//! - UEFI serial debug text
//! - HUD overlays on top of the GPU compositor
//!
//! Full subpixel antialiasing and TrueType rendering live in `sdk/ui-sdk/src/font.rs`.

/// Width of each glyph in pixels.
pub const GLYPH_W: usize = 8;
/// Height of each glyph in pixels.
pub const GLYPH_H: usize = 16;
/// First printable ASCII code included in the atlas.
pub const FIRST_CHAR: u8 = 0x20; // space
/// Last printable ASCII code included.
pub const LAST_CHAR: u8 = 0x7E; // ~
/// Number of glyphs in the atlas.
pub const GLYPH_COUNT: usize = (LAST_CHAR - FIRST_CHAR + 1) as usize;

/// Bitmap atlas — one 8-byte row per pixel row, one entry per glyph.
/// Bit 7 of each byte = leftmost pixel; bit 0 = rightmost pixel.
///
/// The data below is a subset of the classic 8×16 VGA font (public domain).
/// Only printable ASCII is covered.  Missing glyphs (index out of range)
/// are rendered as the replacement character glyph (last slot).
static FONT_ATLAS: [[u8; GLYPH_H]; GLYPH_COUNT] = font_data();

